let sorting = () => {
  pro_count.innerHTML=""
  let select_tag = document.querySelector("#select_tag").value;
  console.log(select_tag)
  
  if (select_tag == "Bestselling") {
    let new_data2=[];
    for (let i = 0; i < new_data.length; i++) {
      if (select_tag == new_data[i].tag) {
        new_data2.push(new_data[i]);
      }
    }

    renderdom(new_data2)
    pro_count.innerText=`${new_data2.length} products`
    // Featured Filter
  } else if (select_tag=="Featured") {
    let new_data2=[];
    for (let i = 0; i < new_data.length; i++) {
      if (select_tag==new_data[i].tag) {
        new_data2.push(new_data[i]);
      }
    }
   
    renderdom(new_data2)
    pro_count.innerText=`${new_data2.length} products`

    // A to Z Filter
  } else if ( select_tag=="A-Z") {
    new_data.sort(function (a, b) {
      if (a.title > b.title) {
        return new_data;
      }
      if (b.title > a.title) {
        return new_data;
      }
      // return 0;
    });
   
    renderdom(new_data)
    pro_count.innerText=`${new_data.length} products`

   
  } else if (select_tag=="Z-A") {
    new_data.sort(function (a, b) {
      if (a.title < b.title) {
        return 1;
      }
      if (b.title < a.title) {
        return -1;
      }
      return 0;
    });
    
    renderdom(new_data)
    pro_count.innerText=`${new_data.length} products`

   
  } else if (select_tag=="low to high") {
    new_data.sort(function (a, b) {
      return a.dolar - b.dolar;
    });
   
    renderdom(new_data)
    pro_count.innerText=`${new_data.length} products`

    // Price High to Low Filter
  } else if (select_tag == "high to low") {
    new_data.sort(function (a, b) {
      return b.dolar - a.dolar;
    });
   
    renderdom(new_data)
    pro_count.innerText=`${new_data.length} products`

    // Old to New Product
  } else if (select_tag == "old new") {
    new_data.sort(function (a, b) {
      return a.date - b.date;
    });
    renderdom(new_data)
    pro_count.innerText=`${new_data.length} products`

    // New to Old Product
  } else if (select_tag == "new old") {
    new_data.sort(function (a, b) {
      return b.date - a.date;
    });
    renderdom(new_data)
    pro_count.innerText=`${new_data.length} products`
  }
};


document.querySelector("#select_tag").addEventListener("change", sorting);


